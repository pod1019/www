import turtle
#设置画笔粗细
turtle.width(10)

#设置画笔颜色
turtle.color("blue")

#开始画圆
turtle.circle(50)

#抬起笔
turtle.penup()
#移动笔
turtle.goto(120,0)
#落笔、下笔
turtle.pendown()
turtle.color("black")
turtle.circle(50)

turtle.penup()
turtle.goto(240,0)
turtle.pendown()
turtle.color("red")
turtle.circle(50)

turtle.penup()
turtle.goto(60,-50)
turtle.pendown()
turtle.color("yellow")
turtle.circle(50)

turtle.penup()
turtle.goto(180,-50)
turtle.pendown()
turtle.color("green")
turtle.circle(50)
